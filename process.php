							<p>When we seek inspiration for our cards we look to natural phenomenon in nature. These natural phenomenon evoke such awe and power that we can only hope to capture through painting. It is the rarest of these natural events that we, at Card Happening, relate to best.</p>
							<p>Take Maelstrom, a powerful water borne whirlpool whose mighty vortex of current could grab a sea vessel and rip it into smithereens. Surely any sea goers worst nightmare. We try to replicate the ferocity of a Maelstrom with thick reds, pinks, and greens sporadically dispersed by an angry sea of blue.</p>
							<p>Where we seek an inspiration of ferocity from Maelstrom we seek an essence of rarity from Mandarin Twilight. Mandarin Twilight is the last few moments of a sunset when the sky transitions from day to night. A sight that no matter how many times you have seen will still make you smile in appreciation. In order to capture this moment, we start with a mandarin background and then begin pulling it away with nightly blues, greens, and reds.</p>
							<p>Skypunch is the phenomenon when ice crystals form in the upper atmosphere fall and puncture through cloud formations allowing light to shine through. To capture the marvelous nature of the puncturing light, we swiftly apply smooth greens and yellows on a otherwise clear day sky.</p>
							<p>Nature provides us a diverse spectrum of inspiration. Just as lightning never strikes the same spot twice and no two snow flakes are the same, each one of our hand-painted cards is one-of-a-kind.</p>
